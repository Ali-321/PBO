class Karyawan{
    String nama;
    String idPegawai;
    char jenisKelamin;
    int tunjanganAnak;
    int jumHariMasuk;
    public Karyawan(String nama,String idPegawai,char jenisKelamin,int tunjanganAnak,int jumHariMasuk){
        this.nama = nama;
        this.idPegawai = idPegawai;
        this.jenisKelamin = jenisKelamin;
        this.tunjanganAnak = tunjanganAnak;
        this.jumHariMasuk = jumHariMasuk;
    }

    
}